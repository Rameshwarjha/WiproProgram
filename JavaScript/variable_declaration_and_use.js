// we can declare variable with single quotes also 
const first_name= 'Raj';   
console.log(first_name);

const last_name= "verma";
console.log(last_name);

// name with less space
// ### how to include "variable in between string"   ** js tricks
const full_name = `My name is ${first_name} ${last_name} `;
console.log(full_name);

// name with more space 
// ### how to include "variable in between string"    ** js tricks

const full_name1 = `My name is ${first_name}        ${last_name} `;
console.log(full_name1);
